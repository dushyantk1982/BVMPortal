<h2 align="center"><u>Diploma In Education </u></h2>
<p align="justify" class="pagefont">The D.Ed.(Elementary Teacher Education programme) Programme was started in 2005-06. The D.Ed.Programme is meant for preparing teachers for elementary schools ( primary and upper primary / middle). is approved by NCTE, New Delhi and Dept. of School Education, Govt.of M.P. Bhopal and affiliated to Madhya Pradesh, Board of Secondary Education, Bhopal. D.Ed. programme is helpful in all round development of students, interested in joining teaching profession. After doing D.Ed. they are well trained for teaching primary classes and are preferred in different States such as Haryana, Rajasthan, Delhi, Madhya Pradesh and in Central Schools.</p>
